// retorna o valor de PI
console.log(Math.PI);

// arredonda pro número inteiro mais perto
console.log(Math.round(3.47));

// arredonda pra baixo
console.log(Math.floor(3.47));

// arredonda pra cima
console.log(Math.ceil(3.47));

// retorna o valo absoluto (positivo) de um número
console.log(Math.abs(-9.6245));

// retorna o menor número indicado
console.log(Math.min(7, 20, 1, 245, 200));

// retorna o maior número indicado
console.log(Math.max(7, 20, 1, 245, 200));

// retorna números aleatórios entre 0 e 1
console.log(Math.random());

